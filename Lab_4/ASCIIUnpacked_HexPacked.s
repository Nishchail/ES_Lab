	AREA RESET, DATA, READONLY
	EXPORT __Vectors

__Vectors
	DCD 0x1001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler

Reset_Handler
	LDR R3 , =DST
	LDR R0, =ASCII ; LOADING THE ASCII ADDRESS
	LDRB R1,[R0] , #1 ; LOADING THE LAST BYTE DATA
	LDRB R2 , [R0]  ; LOADING THE NEXT BYTE DATA IN THE REGISTER 
	
	SUB R1, R1, #0X30; TO GET THE LAST BIT VALUE IF IT IS 38 THEN GETTING 8 ;
	CMP R1 , #9 
	BLS SECOND 
	SUB R1, R1, #7 
	
SECOND 
	SUB R2, R2, #0X30
	CMP R2, #9 
	BLS FINAL 
	SUB R2, R2, #7 
	
FINAL
	LSL R1, R1, #4
	ORR R1, R1,R2
	

	STR R1, [R3]
	
ASCII DCB '3', 'A' 
	AREA mydata, DATA, READWRITE 

DST DCB 0 

	END
	